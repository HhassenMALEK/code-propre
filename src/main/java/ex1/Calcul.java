package ex1;

/**
 * Classe qui propose des service de calcules
 */
public class Calcul {
	//classe sans attribut d'instance

	/**
	 * Méthode qui retourne l'addition de 2 entiers ppassés en paramètres
	 * @param a nombre entier 1
	 * @param b nombre entier 2
	 * @return int
	 */
	public static int addition(int a, int b){
		return a+b;
	}

	/**
	 * Méthode qui retourne la soustraction de 2 entiers passés en paramètres
	 * @param a nombre entier 1
	 * @param b nomre entier 2
	 * @return int
	 */
	public static int soustraction(int a, int b){
		return a-b;
	}
}
